var = input("Digite algo: ")

print("\033[1;32mTipo primitivo:\033[m {}".format(type(var)))
print("\033[0;30mMAISCULO:\033[m {}".format(var.isupper()))
print("\033[4;35mMINUSCULO:\033[m {}".format(var.islower()))
print("\033[1;36mALFABETICO E NUMERICO:\033[m {}".format(var.isalnum()))
print("\033[0;31mNUMERICO:\033[m {}".format(var.isnumeric()))
print("\033[1;33mALFABETICO:\033[m {}".format(var.isalpha()))
print("\033[4;34mASCII:\033[m {}".format(var.isascii()))
print("\033[0;37mDECIMAL:\033[m {}".format(var.isdecimal()))
print("\033[1;31mDIGITO:\033[m {}".format(var.isdigit()))
print("\033[4;30mIDENTIFICADOR:\033[m {}".format(var.isidentifier()))
print("\033[0;35mESPACO:\033[m {}".format(var.isspace()))
print("\033[1;36mPRINTAVEL:\033[m {}".format(var.isprintable()))
print("\033[4;33mTITULO:\033[m {}".format(var.istitle()))
